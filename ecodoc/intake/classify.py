"""Распределение входящих документов: что это, какие данные можно взять,
каким формам пригодится.

Эвристика по ключевым словам текста + имени файла. Работает и без ИИ;
при `--ai` семантику уточняет анализатор (doc_type из модели).
"""
from __future__ import annotations

import re
from dataclasses import dataclass

from ecodoc.parsers.text_extract import ExtractedDoc


@dataclass
class DocClass:
    doc_type: str      # человекочитаемый тип документа
    data: str          # какие данные можно взять
    forms: list[str]   # каким формам пригодится


# (регэксп по тексту+имени) → классификация; порядок = приоритет
_RULES: list[tuple[str, DocClass]] = [
    (r"(акт|справк).{0,200}(утилизац|обезвреж|размещени)|"
     r"(утилизац|обезвреж).{0,120}(акт|справк)",
     DocClass("акт/справка об утилизации (передаче отхода)",
              "ФККО, наименование отхода, масса, дата, контрагент (ИНН, лицензия)",
              ["waste-movement", "waste-report-iii", "cadastre-spb",
               "declaration-nvos", "2tp-waste"])),
    (r"протокол.{0,200}(кха|количественного хим|химическ\w+ анализ)",
     DocClass("протокол КХА",
              "вещества и концентрации, № протокола, дата, лаборатория",
              ["pek", "hazard-class", "nds"])),
    (r"биотест",
     DocClass("протокол биотестирования",
              "кратность разведения, тест-объекты → класс опасности",
              ["hazard-class"])),
    (r"паспорт.{0,80}отход",
     DocClass("паспорт отхода I–IV класса",
              "ФККО, класс опасности, компонентный состав",
              ["waste-movement", "waste-report-iii"])),
    (r"журнал.{0,120}(движени|учет|учёт).{0,80}отход|учет\w*.{0,40}отход",
     DocClass("журнал учёта движения отходов (пр. №1028)",
              "движение по каждому отходу: образовано/передано/размещено",
              ["waste-movement", "2tp-waste", "cadastre-spb"])),
    (r"инвентаризац.{0,120}(выброс|источник)",
     DocClass("инвентаризация источников выбросов",
              "источники, вещества, г/с и т/год",
              ["2tp-air", "air-inventory", "pek"])),
    (r"лиценз.{0,200}(отход|обращени)",
     DocClass("лицензия оператора по обращению с отходами",
              "номер, дата, разрешённые виды деятельности и ФККО",
              ["waste-movement", "waste-report-iii"])),
    (r"договор.{0,200}(отход|вывоз|транспортиров)",
     DocClass("договор на обращение с отходами",
              "контрагент (ИНН), предмет, ФККО",
              ["waste-movement", "waste-report-iii"])),
    (r"выписк.{0,40}(егрюл|егрип)|устав|свидетельств.{0,60}(регистрац|учет)",
     DocClass("учредительные документы / выписка ЕГРЮЛ",
              "реквизиты организации: ИНН, КПП, ОГРН, адрес, руководитель",
              ["declaration-nvos", "pek", "2tp-waste", "2tp-air"])),
    (r"свидетельств.{0,120}нвос|постановк.{0,120}(нвос|государственный учет)",
     DocClass("свидетельство о постановке объекта НВОС на учёт",
              "код объекта НВОС, категория, адрес",
              ["declaration-nvos", "pek"])),
    (r"декларац.{0,120}плат",
     DocClass("декларация о плате за НВОС (прошлый период/эталон)",
              "перечень веществ/отходов, ставки, объект НВОС",
              ["declaration-nvos"])),
    (r"программ.{0,80}(пэк|экологического контроля)",
     DocClass("программа ПЭК",
              "точки контроля, периодичность, показатели",
              ["pek"])),
    # платёжка — раньше generic-«договора»: в платёжках договор упоминается
    (r"плат[её]жн|платежное поручение",
     DocClass("платёжное поручение",
              "суммы авансовых платежей за НВОС (для зачёта)",
              ["declaration-nvos"])),
    (r"договор",
     DocClass("договор",
              "реквизиты сторон (ИНН, адреса)",
              [])),
]


def classify(doc: ExtractedDoc) -> DocClass:
    hay = (doc.path.name + "\n" + doc.text[:8000]).lower().replace("ё", "е")
    for pattern, dc in _RULES:
        if re.search(pattern, hay, re.S):
            return dc
    return DocClass("не распознано — проверьте вручную",
                    "реквизиты/коды при их наличии (ИНН, ФККО, код НВОС)", [])


def render(docs: list[ExtractedDoc]) -> str:
    """Секция отчёта intake: файл → тип → что взять → для каких форм."""
    lines = ["── Распределение файлов: что это и для чего ──"]
    for d in docs:
        c = classify(d)
        lines.append(f"  • {d.path.name}")
        lines.append(f"      тип:    {c.doc_type}")
        lines.append(f"      данные: {c.data}")
        if c.forms:
            lines.append(f"      формы:  {', '.join(c.forms)}")
    return "\n".join(lines)
